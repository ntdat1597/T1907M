package bai_tap_phan_so;

public class Rational
{
    private  int tu;
    private int mau;


    public Rational()
    {

    }

    public Rational(int a , int b)
    {
        this.tu=a;
        this.mau=b;
    }


    public  void setTu(int i)
    {
        this.tu=i;
    }

    public void setMau(int i)
    {
        this.mau=i;
    }

    public int getTu()
    {
        return this.tu;

    }

    public int getMau()
    {
        return this.mau;

    }

}









